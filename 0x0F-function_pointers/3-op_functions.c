#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

//addition function
int op_add(int a, int b)
{
	return (a + b);
}

//subtraction function
int op_sub(int a, int b)
{
	return (a-b);
}

//multiplication function
int op_mul(int a, int b)
{
	return(a*b);
}


//division function
int op_div(int a, int b)
{
	return (a/b);
}

//modulus function
int op_mod(int a, int b)
{
	return (a%b);
}
